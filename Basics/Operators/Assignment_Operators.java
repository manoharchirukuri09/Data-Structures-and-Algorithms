
public class Assignment_Operators {
    public static void main(String[] args) {
      int a = 10;
       a += 5;
       System.out.println("Value of a after addition: " + a);
       int b = 10;
       b *= 5;
       System.out.println("Value of b after multiplication: " + b);
       int c = 15;
       c /= 5;
       System.out.println("Value of c after division: " + c);
       int d = 20;
       d -= 5;
       System.out.println("Value of d after subtraction: " + d);

    }
}
